# https://github.com/RaverJay/nucl2codon/blob/main/nucl2codon.py
def nucl2codon(nucl_pos):
    '''Returns (gene name, codon number) for a nucleotide position on the SARS-CoV-2 genome - or (None, -1) if something goes wrong
    Logic from https://github.com/theosanderson/Codon2Nucleotide
    '''

    # make an int out it
    nucl_pos = int(nucl_pos)

    # fixed gene annotation (yoinked from Codon2Nucleotide, hopefully this is correct for the Wuhan reference)
    genes = {
    'ORF1a': [266, 13467],
    'ORF1b': [13468, 21556],
    'S': [21563, 25385],
    'ORF3a': [25393, 26221],
    'E': [26245, 26473],
    'M': [26523, 27192],
    'ORF6': [27202, 27388],
    'ORF7a': [27394, 27760],
    'ORF7b': [27756, 27888],
    'ORF8': [27894, 28260],
    'N': [28274, 29534],
    'ORF10': [29558, 29675]
    }

    matches = []
    for gene in genes:
        if genes[gene][0] <= nucl_pos <= genes[gene][1]:
            matches.append(gene)
    

    if len(matches) != 1:
        return (None, -1)

    gene = matches[0]
    codon = ((nucl_pos - genes[gene][0]) // 3) + 1
    
    return gene, codon
    

# https://github.com/RaverJay/biostuff/blob/main/nt2aa.py
def nt2aa(nucleotides, allow_uracil=True, allow_stop_codon=False):
    '''Returns amino acid translation for a nucleotide sequence of triplets, and None otherwise
    '''

    if not len(nucleotides)%3 == 0:
        return None

    triplets = {
            'ATA':'I', 'ATC':'I', 'ATT':'I', 'ATG':'M',
            'ACA':'T', 'ACC':'T', 'ACG':'T', 'ACT':'T',
            'AAC':'N', 'AAT':'N', 'AAA':'K', 'AAG':'K',
            'AGC':'S', 'AGT':'S', 'AGA':'R', 'AGG':'R',
            'CTA':'L', 'CTC':'L', 'CTG':'L', 'CTT':'L',
            'CCA':'P', 'CCC':'P', 'CCG':'P', 'CCT':'P',
            'CAC':'H', 'CAT':'H', 'CAA':'Q', 'CAG':'Q',
            'CGA':'R', 'CGC':'R', 'CGG':'R', 'CGT':'R',
            'GTA':'V', 'GTC':'V', 'GTG':'V', 'GTT':'V',
            'GCA':'A', 'GCC':'A', 'GCG':'A', 'GCT':'A',
            'GAC':'D', 'GAT':'D', 'GAA':'E', 'GAG':'E',
            'GGA':'G', 'GGC':'G', 'GGG':'G', 'GGT':'G',
            'TCA':'S', 'TCC':'S', 'TCG':'S', 'TCT':'S',
            'TTC':'F', 'TTT':'F', 'TTA':'L', 'TTG':'L',
            'TAC':'Y', 'TAT':'Y', 'TAA':'*', 'TAG':'*',
            'TGC':'C', 'TGT':'C', 'TGA':'*', 'TGG':'W'
        }

    if allow_uracil:
        nucleotides.replace('U', 'T')

    peptide = ""
    for pos in range(0, len(nucleotides), 3):
        codon = nucleotides[pos:pos+3]
        peptide += triplets[codon]

    # stop codon
    if '*' in peptide:
        return None
    
    return peptide


def insertions_nt_to_aa(nt_ins):
    '''convert Nextclade nucleotide insertion calls to gene/codon number aa insertion format
    e.g. '22205:GAGCCAGAA' -> 'S:R214REPE'
    will skip conversion as a fallback if something goes wrong
    '''
    nt_ins_list = nt_ins.split(',')
    aa_ins_list = []

    for ins in nt_ins_list:
        pos, nucls = ins.split(':')
        pos = int(pos)
        gene, codon = nucl2codon(pos)
        aminos = nt2aa(nucls)
        if gene is None or aminos is None:
            # fallback: no conversion
            aa_ins_list.append(ins)
        else:
            # notation includes the amino acid directly before, e.g. 'S:R214REPE'
            nt_before = get_wuhan_seq_from_pos(pos-3, pos-1)
            aa_before = nt2aa(nt_before)
            aa_ins_list.append(gene + ':' + aa_before + str(codon-1) + aa_before + aminos)

    return ','.join(aa_ins_list)


